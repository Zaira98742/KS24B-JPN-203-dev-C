#include <stdio.h>
int main() {
	// kieu int dung de luu tru so nguyen
	int a = 10;
	
	// kieu float dung de luu tru so thuc
	float b =3.14f;
	
	// kieu double dung de luu tru so thuc co do chinh xac cao hon
	double c = 3.14;
	
	// kieu char dung de luu tru mot ky tu
	char d = 'A';
	
	// kieu short dung de luu tru so nguyen co kich thuoc nho hon int
	short e = 12345;
	
	// kieu long dung de luu tru so nguyen lon hon int
	long f = 1000000;
	
	// bat dau khai bao
	printf("a: %d\n", a);
	printf("b: %f\n", b);
	printf("c: %f\n", c);
	printf("d: %c\n", d);
	printf("e: %d\n", e);
	printf("f: %d\n", f); 
	 
	
	return 0; 
	 
	
	 
}
